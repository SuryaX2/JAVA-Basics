package P1;
public class ABC
{
   private int a=10;
     int b=20;//Default
   protected int c=30;
   public int d=40;
   public void show()
    {
       System.out.println("Private Data = "+a);
       System.out.println("Default Data = "+b);
       System.out.println("Protected Data = "+c);
       System.out.println("Public Data = "+d);
    }
}